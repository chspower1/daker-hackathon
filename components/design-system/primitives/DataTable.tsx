import * as React from "react";
import { cn } from "@/lib/cn";

export type DataTableProps = React.HTMLAttributes<HTMLTableElement>;

export function DataTable({ className, ...props }: DataTableProps) {
  return (
    <div className="w-full overflow-auto rounded-md border border-border-base bg-surface-base">
      <table
        className={cn("w-full caption-bottom text-sm", className)}
        {...props}
      />
    </div>
  );
}

export const TableHeader = React.forwardRef<
  HTMLTableSectionElement,
  React.HTMLAttributes<HTMLTableSectionElement>
>(({ className, ...props }, ref) => (
  <thead ref={ref} className={cn("[&_tr]:border-b border-border-base bg-surface-muted", className)} {...props} />
));
TableHeader.displayName = "TableHeader";

